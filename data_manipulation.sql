-- The `SELECT` statement allows you to retrieve data from one or more tables. Here's the basic syntax




-- The `INSERT` statement lets you add new rows to a table. Here's how it works:
INSERT INTO table_name(column1, column2)
VALUES (value1, value2)




-- The `UPDATE` statement modifies existing data in a table. Use it like this:
UPDATE table_name
SET column1 = value1, column2 = value2
WHERE condition;
-- The `WHERE` clause specifies which rows to update. Without it, all rows will be updated!





-- The `DELETE` command removes records from a table. Here's the syntax:
DELETE FROM table_name
WHERE condition



